    }

    public function findByText($value): array {
        return $this->createQueryBuilder('c')
            ->where('c.name LIKE :value')
            ->setParameter('value', '%' . $value . '%')
            ->orderBy('c.name', 'ASC')
            ->getQuery()
            ->getResult();
    }
